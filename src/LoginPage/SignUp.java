    private void signupbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupbtnActionPerformed
        // TODO add your handling code here:
        try {
            String sql ="INSERT INTO registration_tb(Username, Password, Tearcher_name) VALUES (?,?,?)";
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/employeesystem_db", "root","");
            
            PreparedStatement ps = conn.prepareStatement(sql);
            
            String username =usernametxt.getText();
            String password = passwordtxt.getText();
            String teacher = teachertxt.getText();
            
            if (isRecordExists(username, teacher)) {
            JOptionPane.showMessageDialog(null, "Username or Teacher already exists in the database.");
            return; // Exit method if record exists
        }
            
            ps.setString(1, usernametxt.getText());
            ps.setString(2, passwordtxt.getText());
            ps.setString(3, teachertxt.getText());
            
            
            int rowsAffected = ps.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(null, "Data stored successfully");
            } else {
                    JOptionPane.showMessageDialog(null, "Failed to store data");
                }
            usernametxt.setText("");
            passwordtxt.setText("");
            teachertxt.setText("");
            
            
            
        } catch (Exception e) {
            System.out.println("error--->"+e.getMessage());
        }
    }//GEN-LAST:event_signupbtnActionPerformed

