    private boolean isRecordExists(String username, String teacher) {
    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/employeesystem_db", "root", "");
        String sql = "SELECT * FROM registration_tb WHERE Username = ? OR Tearcher_name = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, username);
        ps.setString(2, teacher);
        ResultSet rs = ps.executeQuery();
        return rs.next(); // If rs.next() returns true, record exists
    } catch (Exception e) {
        System.out.println("Error checking record existence: " + e.getMessage());
        return false;
    }
}

